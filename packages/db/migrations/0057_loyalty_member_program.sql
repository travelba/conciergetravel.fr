-- 2. Update the auto-create trigger to insert the new default tier
----------------------------------------------------------------

create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_locale text;
  v_display text;
begin
  v_locale := coalesce(new.raw_user_meta_data ->> 'locale_pref', 'fr');
  if v_locale not in ('fr', 'en') then
    v_locale := 'fr';
  end if;

  v_display := nullif(trim(new.raw_user_meta_data ->> 'display_name'), '');

  insert into public.profiles (id, display_name, locale_pref, newsletter_opt_in)
  values (
    new.id,
    v_display,
    v_locale,
    coalesce((new.raw_user_meta_data ->> 'newsletter_opt_in')::boolean, false)
  )
  on conflict (id) do nothing;

  insert into public.loyalty_members (id, tier, total_bookings)
  values (new.id, 'club', 0)
  on conflict (id) do nothing;

  return new;
end;
$$;

-- SECURITY DEFINER hardening per .cursor/rules/supabase-rls.mdc.
revoke execute on function public.handle_new_auth_user() from public;
revoke execute on function public.handle_new_auth_user() from anon;
revoke execute on function public.handle_new_auth_user() from authenticated;

