-- 0057 — Le Concierge Club foundations.
--
-- Sprint 1 of the membership program (see plan
-- `.cursor/plans/le_cercle_membership_program_dc0717ed.plan.md`).
-- This migration is the **single forward-only DB change** for the
-- entire Phase 1 of Le Concierge Club. Everything that follows
-- (Stripe billing, Little API sync, member price differential) is
-- additive and ships in its own future migration in Phase 6.
--
-- What this migration does, in order:
--
--   1. Loyalty tier rename : `free → club`, `premium → prestige`.
--      The original 0001 migration used a CHECK constraint (not an
--      enum), so we drop+rewrite the constraint and UPDATE rows in
--      place. The 0003 `handle_new_auth_user` trigger is rewritten to
--      insert the new default `'club'`.
--
--   2. `loyalty_members` gains the Phase 6 billing columns up front
--      (trial_*, paid_until, stripe_*) so Sprint 1 code can reference
--      a stable shape and Phase 6 only needs to flip a feature flag
--      and wire the Stripe webhook — no migration churn.
--
--   3. New tables (all empty in Phase 1):
--        - `hotel_member_benefits`   — per-hotel benefit reality,
--          populated by the Little API sync in Phase 6 (source='little_api'),
--          or by addendum data (source='manual_addendum'). Phase 1
--          renders the maximalist catalogue client-side from a hardcoded
--          config; the table exists for forward compatibility.
--        - `member_price_differential` — price discount per hotel/room/
--          tier, populated by the Amadeus offer-time cron in Phase 6.
--        - `club_eligibility`        — opt-in flag per hotel for the
--          program, plus the addendum signed timestamp.
--        - `prestige_waitlist`       — anonymous + member email capture
--          before Prestige goes live in Phase 6.
--
--   4. `hotels` editorial columns for the 4 enriched concierge sections
--      shipped in Sprint 2 (conseil enrichi 200-300 mots, quartier,
--      gastronomie, timing/accès). Each carries an `_editorial_review_status`
--      so the back-office UX (Sprint 1 Payload work) can route drafts to
--      human fact-check before publish.
--
--   5. RLS + covering indexes for every FK + grants. The internal
--      `_cct_sql_migrations` log row at the end is mandatory per
--      `.cursor/rules/supabase-rls.mdc`.
--
-- Editorial review status convention:
--   * 'draft'    — generated by the LLM pipeline, awaiting human review
--   * 'pending'  — flagged for review by an editor (fact-check queue)
--   * 'approved' — human-validated, safe to render
--
-- Forward-only: do NOT edit this migration after it ships. If we need
-- to undo any of these changes, create a new 00NN_revert_<thing>.sql.

----------------------------------------------------------------
-- 1. loyalty_members — tier rename + billing columns
----------------------------------------------------------------

-- Drop the old check constraint and rewrite (the original 0001 used
-- a plain text + CHECK pattern, not a Postgres enum, so a simple
-- UPDATE + ALTER CONSTRAINT is enough).
alter table public.loyalty_members
  drop constraint if exists loyalty_members_tier_ck;

-- Rename in place. ON CONFLICT not needed (tier is non-unique).
update public.loyalty_members
   set tier = 'club'
 where tier = 'free';

update public.loyalty_members
   set tier = 'prestige'
 where tier = 'premium';

alter table public.loyalty_members
  alter column tier set default 'club',
  add constraint loyalty_members_tier_ck
    check (tier in ('club', 'prestige'));

-- Phase 6 billing columns — added now so Sprint 1 code typechecks
-- against the final shape. All nullable, no backfill needed.
alter table public.loyalty_members
  add column if not exists trial_started_at      timestamptz,
  add column if not exists trial_ends_at         timestamptz,
  add column if not exists paid_until            timestamptz,
  add column if not exists stripe_subscription_id text,
  add column if not exists stripe_customer_id    text,
  add column if not exists cancelled_at          timestamptz,
  add column if not exists cancellation_reason   text;

-- Covering indexes used by Phase 6 cron jobs (trial expiry, renewal).
create index if not exists loyalty_members_tier_idx
  on public.loyalty_members (tier);

create index if not exists loyalty_members_trial_ends_at_idx
  on public.loyalty_members (trial_ends_at)
  where trial_ends_at is not null;

create index if not exists loyalty_members_paid_until_idx
  on public.loyalty_members (paid_until)
  where paid_until is not null;

create unique index if not exists loyalty_members_stripe_subscription_id_uk
  on public.loyalty_members (stripe_subscription_id)
  where stripe_subscription_id is not null;

