-- 3. hotel_member_benefits — per-hotel benefit reality
----------------------------------------------------------------
--
-- Phase 1 : empty. The <ClubBenefitsBlock> renders the maximalist
-- catalogue from a hardcoded client-side config.
-- Phase 6 : populated nightly by the Little API sync
-- (`packages/integrations/little-hotelier/sync/`), with addendum-only
-- entries for hotels that have a signed legal addendum (source='manual_addendum').

create table if not exists public.hotel_member_benefits (
  id           uuid primary key default gen_random_uuid(),
  hotel_id     uuid not null references public.hotels (id) on delete cascade,
  tier         text not null,
  benefit_code text not null,
  description_fr text,
  description_en text,
  is_subject_to_availability boolean not null default false,
  sort_order   integer not null default 100,
  source       text not null default 'default',
  valid_from   timestamptz,
  valid_until  timestamptz,
  created_at   timestamptz not null default timezone('utc', now()),
  updated_at   timestamptz not null default timezone('utc', now()),

  constraint hotel_member_benefits_tier_ck
    check (tier in ('club', 'prestige')),
  constraint hotel_member_benefits_source_ck
    check (source in ('default', 'manual_addendum', 'little_api')),
  constraint hotel_member_benefits_unique
    unique (hotel_id, tier, benefit_code)
);

create index if not exists hotel_member_benefits_hotel_id_idx
  on public.hotel_member_benefits (hotel_id);

create index if not exists hotel_member_benefits_tier_idx
  on public.hotel_member_benefits (tier);

create trigger hotel_member_benefits_set_updated_at
  before update on public.hotel_member_benefits
  for each row execute function public.set_updated_at();

