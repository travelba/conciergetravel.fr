-- 4. member_price_differential — empty Phase 1, wired Phase 6
----------------------------------------------------------------

create table if not exists public.member_price_differential (
  id           uuid primary key default gen_random_uuid(),
  hotel_id     uuid not null references public.hotels (id) on delete cascade,
  room_id      uuid references public.hotel_rooms (id) on delete cascade,
  tier         text not null,
  -- discount in basis points (e.g. 1000 = -10.00%). Avoid floats.
  discount_bps integer not null,
  -- absolute minimum member price in minor units (cents). Used to clamp
  -- the displayed differential when the upstream price is unusually low.
  min_price_minor integer,
  source       text not null default 'amadeus_offer',
  valid_from   timestamptz,
  valid_until  timestamptz,
  created_at   timestamptz not null default timezone('utc', now()),
  updated_at   timestamptz not null default timezone('utc', now()),

  constraint member_price_differential_tier_ck
    check (tier in ('club', 'prestige')),
  constraint member_price_differential_discount_ck
    check (discount_bps >= 0 and discount_bps <= 10000),
  constraint member_price_differential_source_ck
    check (source in ('amadeus_offer', 'little_api', 'manual'))
);

create index if not exists member_price_differential_hotel_id_idx
  on public.member_price_differential (hotel_id);

create index if not exists member_price_differential_room_id_idx
  on public.member_price_differential (room_id)
  where room_id is not null;

create index if not exists member_price_differential_tier_idx
  on public.member_price_differential (tier);

create trigger member_price_differential_set_updated_at
  before update on public.member_price_differential
  for each row execute function public.set_updated_at();

